# Упражнение 8.
# Вы загадываете число от 1 до 100 (включительно). 
# Компьютер спрашивает у вас «Твое число равно, меньше или больше, чем число N?»,  где N — число, которое хочет проверить компьютер.
# Вы отвечаете одним из трёх чисел:
# 1 — равно,
# 2 — больше,
# 3 — меньше.

# Напишите программу, которая с помощью цепочки таких вопросов и ответов угадывает число.
# Дополнительно: сделайте так, чтобы можно было гарантированно угадать число за семь попыток.

def guess_number():
    low, high = 1, 100
    attempts = 0
    print("Загадайте число от 1 до 100 (включительно). Я постараюсь его угадать за 7 попыток.")
    
    while attempts < 7:
        mid = (low + high) // 2
        attempts += 1
        print(f"Попытка {attempts}: Твое число равно, меньше или больше, чем число {mid}?")
        response = input("Введите 1 (равно), 2 (больше), 3 (меньше): ")
        
        if response == '1':
            print(f"Ура! Я угадал твое число {mid} за {attempts} попыток!")
            return
        elif response == '2':
            low = mid + 1
        elif response == '3':
            high = mid - 1
        else:
            print("Пожалуйста, введите корректный ответ (1, 2 или 3).")
            attempts -= 1  # Не засчитываем некорректный ввод как попытку
    
    print("К сожалению, не удалось угадать число за 7 попыток.")

guess_number()

