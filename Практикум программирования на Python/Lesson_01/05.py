# Задача 5.
# Дано уравнение ax + b = 0 и отрезок [m;n]. 
# Ответьте на вопрос, попадает ли решение уравнения в указанный отрезок.


a = float(input("Введите a: "))
b = float(input("Введите b: "))

m = float(input("Введите m (нижняя граница): "))
n = float(input("Введите n (верхняя граница): "))

# Проверка, что a не равно 0
if a == 0:
    if b == 0:
        print("Уравнение имеет бесконечно много решений, все они в отрезке.")
    else:
        print("Уравнение не имеет решений.")
else:
    x = -b / a
    print(f"Решение уравнения: x = {x}")
    
    # Проверка принадлежности отрезку
    if m <= x <= n:
        print(f"Решение попадает в отрезок [{m}; {n}].")
    else:
        print(f"Решение не попадает в отрезок [{m}; {n}].")